    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    private String carNo;

    public String getCarNo() {
        return this.carNo;
    }

    public void setCarNo(String carNo) {
        this.carNo = carNo;
    }

